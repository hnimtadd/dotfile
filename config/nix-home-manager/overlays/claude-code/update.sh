#!/usr/bin/env nix-shell
#!nix-shell -i bash -p nodePackages.npm nix-prefetch-git jq

set -euo pipefail

echo "Updating claude-code overlay..."

# Get the latest version from npm
version=$(npm view @anthropic-ai/claude-code version)
echo "Latest version: $version"

# Generate updated lock file
cd "$(dirname "${BASH_SOURCE[0]}")"
npm i --package-lock-only @anthropic-ai/claude-code@"$version"
rm -f package.json

# Generate source hash
echo "Generating source hash..."
src_hash=$(nix-prefetch-url --type sha256 "https://registry.npmjs.org/@anthropic-ai/claude-code/-/claude-code-$version.tgz")
echo "Source hash: $src_hash"

# Generate npm deps hash using nix-prefetch-url on the package-lock.json
echo "Generating npm deps hash..."
npm_deps_hash=$(nix-prefetch-url file://$PWD/package-lock.json)
echo "NPM deps hash: $npm_deps_hash"

# Update the overlay file
echo "Updating overlay file..."
cd ../..

# Create a temporary file with the updated content
cat > overlays/claude-code/default.nix.tmp << EOF
{ lib
, buildNpmPackage
, fetchzip
, nodejs_20
, writeShellScript
}:

let
  # Manual version and hashes - update these when you want a new version
  version = "$version";
  srcHash = "$src_hash";
  depsHash = "$npm_deps_hash";

  # Fetch the source tarball
  src = fetchzip {
    url = "https://registry.npmjs.org/@anthropic-ai/claude-code/-/claude-code-\${version}.tgz";
    hash = srcHash;
  };

in
buildNpmPackage rec {
  pname = "claude-code";
  inherit version src;

  nodejs = nodejs_20; # required for sandboxed Nix builds on Darwin

  depsHash = depsHash;

  # Use the package-lock.json file generated by the update script
  postPatch = ''
    cp \${./package-lock.json} package-lock.json
  '';

  dontNpmBuild = true;

  AUTHORIZED = "1";

  # \`claude-code\` tries to auto-update by default, this disables that functionality.
  # https://docs.anthropic.com/en/docs/agents-and-tools/claude-code/overview#environment-variables
  postInstall = ''
    wrapProgram \$out/bin/claude \\
      --set DISABLE_AUTOUPDATER 1
  '';

  # Helper script to generate new hashes
  passthru.updateScript = writeShellScript "update-claude-code" ''
    set -e
    echo "Claude-code overlay update helper..." >&2
    echo "" >&2
    echo "To update to a new version:" >&2
    echo "1. Run: ./overlays/claude-code/update.sh" >&2
    echo "2. This will automatically:" >&2
    echo "   - Fetch the latest version from npm" >&2
    echo "   - Generate package-lock.json" >&2
    echo "   - Update version and hashes" >&2
    echo "" >&2
    echo "Current version: \${version}" >&2
    echo "Current src hash: \${srcHash}" >&2
    echo "Current npm deps hash: \${depsHash}" >&2
  '';

  meta = {
    description = "An agentic coding tool that lives in your terminal, understands your codebase, and helps you code faster";
    homepage = "https://github.com/anthropics/claude-code";
    downloadPage = "https://www.npmjs.com/package/@anthropic-ai/claude-code";
    license = lib.licenses.unfree;
    maintainers = [ lib.maintainers.malo ];
    mainProgram = "claude";
  };
}
EOF

# Replace the original file
mv overlays/claude-code/default.nix.tmp overlays/claude-code/default.nix

echo "Update complete! Version $version has been applied."
echo "Run 'make switch' to apply the changes to your system."
